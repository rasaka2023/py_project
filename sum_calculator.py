# Asking or prompting the user to enter an integer number
number1 = int(input("Please enter an integer number: "))
# Printing out the number that will be entered 
print(f"You entered: {number1}")

# Asking or prompting the user to enter a float number
number2 = float(input("Please enter an floating number:"))
# Print the float number that will be entered
print(f"You entered: {number2}")

# Define a variable name sum
sum = number1 + number2

# Print the result/ addition of the variable above
print(sum)
